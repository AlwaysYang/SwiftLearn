import Foundation
import UIKit


func BAKit_Define_Font_SystemSize(fontSize:CGFloat) -> UIFont {
    return UIFont.systemFont(ofSize: fontSize)
}

func BAKit_Define_Font_BoldSystemSize(fontSize:CGFloat) -> UIFont {
    return UIFont.boldSystemFont(ofSize: fontSize)
}

